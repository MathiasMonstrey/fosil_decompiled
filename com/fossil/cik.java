package com.fossil;

public class cik {
    private float cdW;
    private float cdX;

    public cik(float f, float f2) {
        this.cdW = f;
        this.cdX = f2;
    }

    public float Yy() {
        return this.cdW;
    }

    public void ar(float f) {
        this.cdW = f;
    }

    public float Yz() {
        return this.cdX;
    }

    public void as(float f) {
        this.cdX = f;
    }
}
